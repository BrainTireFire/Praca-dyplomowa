export function DamageType(damageTypeNumber: number): string {
  const damageTypes = [
    "acid",
    "bludgeoning",
    "cold",
    "fire",
    "force",
    "lightning",
    "necrotic",
    "piercing",
    "poison",
    "psychic",
    "radiant",
    "slashing",
    "thunder",
  ];

  return damageTypes[damageTypeNumber] || "unknown";
}
